/**
 * Time units of measure
 * @enum
 */
export enum TimeUnit {
  /**
   * Time years.
   */
  YEARS = 1,

  /**
   * Time in months.
   */
  MONTHS = 2,

  /**
   * Time in weeks.
   */
  WEEKS = 3,

  /**
   * Time in days.
   */
  DAYS = 4,

  /**
   * Time in hours.
   */
  HOURS = 5,

  /**
   * Time in minutes.
   */
  MINUTES = 6,

  /**
   * Time in seconds.
   */
  SECONDS = 7,

  /**
   * Time in milliseconds.
   */
  MILLISECONDS = 8,
}
