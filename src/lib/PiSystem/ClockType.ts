/**
 * The various on-board clock types.
 */
export enum ClockType {
  /**
   * The ARM clock.
   */
  ARM = 'ARM',

  /**
   * The core clock.
   */
  CORE = 'Core',

  /**
   * The H264 clock.
   */
  H264 = 'H264',

  /**
   * The ISP clock.
   */
  ISP = 'ISP',

  /**
   * The V3D clock.
   */
  V3D = 'V3D',

  /**
   * The UART clock.
   */
  UART = 'UART',

  /**
   * The PWM clock.
   */
  PWM = 'PWM',

  /**
   * The EMMC clock.
   */
  EMMC = 'EMMC',

  /**
   * The pixel clock.
   */
  PIXEL = 'Pixel',

  /**
   * The VEC clock.
   */
  VEC = 'VEC',

  /**
   * The HDMI clock.
   */
  HDMI = 'HDMI',

  /**
   * The DPI clock.
   */
  DPI = 'DPI',
}
