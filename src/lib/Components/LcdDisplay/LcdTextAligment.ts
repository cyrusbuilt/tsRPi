/**
 * LCD text alignment.
 */
export enum LcdTextAlignment {
  /**
   * Align the text to the center of the current line.
   */
  CENTER = 0,

  /**
   * Align the text to the left-hand side of the current line.
   */
  LEFT = -1,

  /**
   * Align the text to the right-hand side of the current line.
   */
  RIGHT = 1,
}
