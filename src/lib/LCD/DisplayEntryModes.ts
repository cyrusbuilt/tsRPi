/**
 * Display entry modes
 * @enum
 */
export enum DisplayEntryModes {
  /**
   * Cursor entry right.
   */
  ENTRY_RIGHT = 0x00,

  /**
   * Cursor entry left.
   */
  ENTRY_LEFT = 0x02,

  /**
   * Entry shift increment.
   */
  ENTRY_SHIFT_INCREMENT = 0x01,

  /**
   * Entry shift decrement.
   */
  ENTRY_SHIFT_DECREMENT = 0x00,
}
