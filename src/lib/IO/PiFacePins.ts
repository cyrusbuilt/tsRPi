/**
 * PiFace I/O pins.
 * @enum
 */
export enum PiFacePins {
    /**
     * Output pin 1 (relay 1).
     */
    OUTPUT00 = 1,

    /**
     * Output pin 2 (relay 2).
     */
    OUTPUT01 = 2,

    /**
     * Output pin 3.
     */
    OUTPUT02 = 4,

    /**
     * Output pin 4.
     */
    OUTPUT03 = 8,

    /**
     * Output pin 5.
     */
    OUTPUT04 = 16,

    /**
     * Output pin 6.
     */
    OUTPUT05 = 32,

    /**
     * Output pin 7.
     */
    OUTPUT06 = 64,

    /**
     * Output pin 8.
     */
    OUTPUT07 = 128,

    /**
     * Input pin 1 (switch 1).
     */
    INPUT00 = 1001,

    /**
     * Input pin 2 (switch 2).
     */
    INPUT01 = 1002,

    /**
     * Input pin 3 (switch 3).
     */
    INPUT02 = 1004,

    /**
     * Input pin 4 (switch 4).
     */
    INPUT03 = 1008,

    /**
     * Input pin 5.
     */
    INPUT04 = 1016,

    /**
     * Input pin 6.
     */
    INPUT05 = 1032,

    /**
     * Input pin 7.
     */
    INPUT06 = 1064,

    /**
     * Input pin 8.
     */
    INPUT07 = 1128,

    /**
     * No pin assignment.
     */
    NONE = 0
}